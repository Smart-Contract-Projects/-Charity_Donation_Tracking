let provider, signer, contract;

document.addEventListener('DOMContentLoaded', async () => {
  if (typeof window.ethereum !== 'undefined') {
    provider = new ethers.providers.Web3Provider(window.ethereum);
    signer = provider.getSigner();
    contract = new ethers.Contract(contractAddress, abi, signer);

    setupDonorDashboard();
  } else {
    alert('MetaMask is not installed. Please install MetaMask to use this platform.');
  }
});

function setupDonorDashboard() {
  fetchVerifiedCharities();

  const donationFormSection = document.getElementById('donation-form-section');
  const charityListSection = document.getElementById('charity-list-section');
  const donationForm = document.getElementById('donation-form');
  const cancelDonationBtn = document.getElementById('cancel-donation');

  cancelDonationBtn.addEventListener('click', () => {
    donationFormSection.style.display = 'none';
    charityListSection.style.display = 'block';
  });

  donationForm.addEventListener('submit', async (event) => {
    event.preventDefault();

    const charityId = document.getElementById('donation-charity-id').value;
    const amount = document.getElementById('donation-amount').value;

    try {
      const tx = await contract.donate(charityId, { value: ethers.utils.parseEther(amount) });
      await tx.wait();

      alert('Donation successful!');
      donationForm.reset();
      donationFormSection.style.display = 'none';
      charityListSection.style.display = 'block';
      fetchVerifiedCharities(); // Refresh the charity list to reflect updated donations
    } catch (error) {
      console.error('Error making donation:', error);
      alert('Failed to make donation. Please try again.');
    }
  });
}

async function fetchVerifiedCharities() {
  const charityList = document.getElementById('charity-list');
  charityList.innerHTML = ''; // Clear the table

  try {
    const charityCount = await contract.charityCount();
    for (let i = 1; i <= charityCount; i++) {
      const charity = await contract.charities(i);
      if (charity.isVerified) {
        const row = document.createElement('tr');
        row.innerHTML = `
          <td>${charity.id}</td>
          <td>${charity.name}</td>
          <td>${charity.cause}</td>
          <td>${ethers.utils.formatEther(charity.totalDonations)} ETH</td>
          <td>${charity.isVerified ? 'Verified' : 'Pending Verification'}</td>
          <td>
            <button class="btn btn-primary btn-sm" onclick="selectCharity(${charity.id})">Donate</button>
          </td>
        `;
        charityList.appendChild(row);
      }
    }
  } catch (error) {
    console.error('Error fetching verified charities:', error);
    alert('Failed to fetch charities. Please try again.');
  }
}

function selectCharity(charityId) {
  const donationFormSection = document.getElementById('donation-form-section');
  const charityListSection = document.getElementById('charity-list-section');

  document.getElementById('donation-charity-id').value = charityId;

  charityListSection.style.display = 'none';
  donationFormSection.style.display = 'block';
}
