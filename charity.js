  let provider, signer, contract;
  
  document.addEventListener('DOMContentLoaded', async () => {
    if (typeof window.ethereum !== 'undefined') {
      provider = new ethers.providers.Web3Provider(window.ethereum);
      signer = provider.getSigner();
      contract = new ethers.Contract(contractAddress, abi, signer);
  
      setupCharityDashboard();
    } else {
      alert('MetaMask is not installed. Please install MetaMask to use this platform.');
    }
  });
  
  function setupCharityDashboard() {
    const registerCharityBtn = document.getElementById('register-charity-btn');
    const viewMyCharitiesBtn = document.getElementById('view-my-charities-btn');
    const registerCharitySection = document.getElementById('register-charity-section');
    const myCharitiesSection = document.getElementById('my-charities-section');
  
    registerCharityBtn.addEventListener('click', () => {
      registerCharitySection.style.display = 'block';
      myCharitiesSection.style.display = 'none';
    });
  
    viewMyCharitiesBtn.addEventListener('click', async () => {
      await fetchMyCharities();
      registerCharitySection.style.display = 'none';
      myCharitiesSection.style.display = 'block';
    });
  
    const form = document.getElementById('charity-registration-form');
    form.addEventListener('submit', async (event) => {
      event.preventDefault();
  
      const name = document.getElementById('charity-name').value;
      const description = document.getElementById('charity-description').value;
      const cause = document.getElementById('charity-cause').value;
      const bankAccount = document.getElementById('charity-bank-account').value;
      const bankName = document.getElementById('charity-bank-name').value;
  
      try {
        const tx = await contract.registerCharity(name, description, bankAccount, bankName, cause);
        await tx.wait();
  
        alert('Charity registered successfully!');
        form.reset();
  
        // Automatically switch to view my charities after registration
        await fetchMyCharities();
        registerCharitySection.style.display = 'none';
        myCharitiesSection.style.display = 'block';
      } catch (error) {
        console.error('Error registering charity:', error);
        alert('Failed to register charity. Please try again.');
      }
    });
  }
  
 async function fetchMyCharities() {
  try {
    const myAddress = await signer.getAddress();
    const tbody = document.getElementById('my-charities-list');
    tbody.innerHTML = ''; // Clear the table

    const charityCount = await contract.charityCount();
    for (let i = 1; i <= charityCount; i++) {
      const charity = await contract.charities(i);
      if (charity.walletAddress.toLowerCase() === myAddress.toLowerCase()) {
        // Convert totalDonations from Wei to Ether and format it
        const totalDonationsEth = ethers.utils.formatEther(charity.totalDonations);
        const formattedDonations = parseFloat(totalDonationsEth).toFixed(3); // Limit to 3 decimals

        const row = document.createElement('tr');
        row.innerHTML = `
          <td>${charity.id}</td>
          <td>${charity.name}</td>
          <td>${charity.cause}</td>
          <td>${formattedDonations} ETH</td> <!-- Updated to display formatted donations -->
          <td>${charity.isVerified ? 'Verified' : 'Pending Verification'}</td>
        `;
        tbody.appendChild(row);
      }
    }
  } catch (error) {
    console.error('Error fetching charities:', error);
    alert('Failed to fetch charities. Please try again.');
  }
}
